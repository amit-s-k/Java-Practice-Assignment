package com.stackroute.main.java.pe1.problem2;

class RangeChecker {
    /**
     * Check whether number is between 20 and 30
     * @param number- integer
     * @return Returns true if the number is between 20 and 30 (both inclusive)
     */
    boolean isNumberBetweenTwentyAndThirty(int number) {
        return number >= 20 && number <= 30;
    }
}
